package hu.oe.nik.szfmv.automatedcar.model;

public class ReferencePoint {
    protected String name;
    protected int x;
    protected int y;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
